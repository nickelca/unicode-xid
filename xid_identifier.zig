//! Determine if a unicode codepoint is a valid identifier for a lexer according
//! to Unicode Standard Annex #31 (http://www.unicode.org/reports/tr31/)

/// Determine if unicode codepoint has the XID_Start property as specified
/// by http://unicode.org/reports/tr31/#NFKC_Modifications
pub fn Is_XID_Start(c: u21) bool {
    return std.sort.binarySearch(
        Range,
        xid_start_ranges,
        c,
        Search_Compare,
    ) != null;
}

/// Determine if unicode codepoint has the XID_Continue property as specified
/// by http://unicode.org/reports/tr31/#NFKC_Modifications
pub fn Is_XID_Continue(c: u21) bool {
    return std.sort.binarySearch(
        Range,
        xid_continue_ranges,
        c,
        Search_Compare,
    ) != null;
}

test Is_XID_Start {
    const yes = [_]u21{ 'A', 'Z', 'a', 'z', '\u{1000d}', '\u{10026}' };
    const no = [_]u21{ '\x00', '\x01', '0', '9', ' ', '[', '<', '{', '(', '\u{02c2}', '\u{ffff}' };

    for (yes) |c| {
        try std.testing.expect(Is_XID_Start(c));
    }

    for (no) |c| {
        try std.testing.expect(!Is_XID_Start(c));
    }
}

test Is_XID_Continue {
    const yes = [_]u21{ '0', '9', 'A', 'Z', 'a', 'z', '_', '\u{1000d}', '\u{10026}' };
    const no = [_]u21{ '\x00', '\x01', ' ', '[', '<', '{', '(', '\u{02c2}', '\u{ffff}' };

    for (yes) |c| {
        try std.testing.expect(Is_XID_Continue(c));
    }

    for (no) |c| {
        try std.testing.expect(!Is_XID_Continue(c));
    }
}

const Range = struct {
    min: u21,
    max: u21,

    pub fn format(
        range: Range,
        comptime fmt: []const u8,
        opts: std.fmt.FormatOptions,
        writer: anytype,
    ) !void {
        _ = fmt;
        _ = opts;
        try writer.print(".{{ .min = '\\u{{{x}}}', .max = '\\u{{{x}}}' }}", .{
            range.min, range.max,
        });
    }
};

fn Search_Compare(c: u21, range: Range) std.math.Order {
    if (c < range.min) {
        // ascii small and more common, so .lt results favored
        @branchHint(.likely);
        return .lt;
    } else if (range.max < c) {
        return .gt;
    } else {
        return .eq;
    }
}

